def t(key: str, lang: str = "EN", **kwargs) -> str:
    """Get translated string for the given key and language."""
    text = TRANSLATIONS.get(lang, TRANSLATIONS["EN"]).get(key, TRANSLATIONS["EN"].get(key, key))
    if kwargs:
        text = text.format(**kwargs)
    return text