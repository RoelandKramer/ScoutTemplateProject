def _info_tooltip_html(desc: dict) -> str:
    """Build the HTML for a competency info tooltip card."""
    name = desc.get("name", "")
    description = desc.get("description", "")
    criteria = desc.get("criteria", [])
    if not description and not criteria:
        return ""
    desc_html = f'<div class="ci-desc">&ldquo;{description}&rdquo;</div>' if description else ""
    li_items = "".join(f"<li>{c}</li>" for c in criteria if c and c != "?")
    criteria_html = ""
    if li_items:
        criteria_html = (
            '<div class="ci-body">'
            '<h5>How do we assess this?</h5>'
            f'<ul>{li_items}</ul>'
            '</div>'
        )
    return (
        '<span class="comp-info-wrap">'
        '<span class="comp-info-icon">i</span>'
        '<div class="comp-info-card">'
        f'<div class="ci-header"><h4>{name}</h4></div>'
        f'{desc_html}'
        f'{criteria_html}'
        '</div>'
        '</span>'
    )


def competency_sections(
    variables,
    key_prefix,
    defaults_stars=None,
    defaults_comments=None,
    defaults_videos=None,
    comp_descriptions=None,
    template_name=None,
):
    L = _lang()
    n = len(variables)
    if defaults_stars is None:
        defaults_stars = [0.0] * n
    if defaults_comments is None:
        defaults_comments = [""] * n
    if defaults_videos is None:
        defaults_videos = [None] * n
    if comp_descriptions is None:
        comp_descriptions = []

    star_values, comments, video_data = [], [], []

    # Find the longest variable name so all (i) icons align at the same
    # fixed horizontal position (the user wants them in a neat column).
    longest_label = max(variables, key=len) if variables else ""

    for i, var in enumerate(variables):
        video_key   = f"{key_prefix}_{i}_video"
        comment_key = f"{key_prefix}_{i}_comment"
        # Handle pending accept BEFORE the text_area widget is instantiated
        accept_flag = f"{key_prefix}_{i}_accept_pending"
        if st.session_state.pop(accept_flag, None):
            suggestion_key = f"{key_prefix}_{i}_suggestion"
            st.session_state[comment_key] = st.session_state.get(suggestion_key, "")
            st.session_state[suggestion_key] = ""
        if comment_key not in st.session_state:
            st.session_state[comment_key] = defaults_comments[i] if i < len(defaults_comments) else ""
        if video_key not in st.session_state:
            st.session_state[video_key] = defaults_videos[i] if i < len(defaults_videos) else None

        # Plain label for all profiles (no caps, no emoji)
        display_label = var

        # Show info tooltip if description is available for this competency
        desc = comp_descriptions[i] if i < len(comp_descriptions) else {}
        info_html = _info_tooltip_html(desc)

        if info_html:
            # Create a strictly decreasing z-index so top rows layer over bottom rows
            z_index = 999 - i

            # Use a hidden ruler matching the LONGEST label so all (i)
            # icons line up at the same horizontal position.
            st.markdown(
                f'<div style="position:absolute; z-index:{z_index}; margin-top:13px; display:flex; align-items:center; pointer-events:none;">'
                f'<div style="visibility:hidden; font-family:-apple-system, BlinkMacSystemFont, sans-serif; font-weight:600; font-size:14px; padding-left:46px; white-space:pre; margin:0;">{longest_label}</div>'
                f'<div style="pointer-events:auto; margin-left:8px;">{info_html}</div>'
                f'</div>',
                unsafe_allow_html=True,
            )

        with st.expander(display_label, expanded=False):
            uploaded_video = st.file_uploader(
                t("video_clip", L),
                type=["mp4", "mov", "avi"],
                key=f"{key_prefix}_{i}_uploader",
            )
            if uploaded_video is not None:
                st.session_state[video_key] = (uploaded_video.getvalue(), uploaded_video.name)

            current_video = st.session_state[video_key]
            if current_video is not None:
                vbytes, vname = current_video
                size_mb = len(vbytes) / (1024 * 1024)
                st.caption(f"Video: **{vname}** ({size_mb:.1f} MB)")
                if len(vbytes) <= _VIDEO_PREVIEW_LIMIT:
                    st.video(vbytes)
                else:
                    st.info(t("too_large_preview", L, mb=size_mb))

            st.markdown(f"**{t('rating', L)}:**")
            val = star_selector(var, key=f"{key_prefix}_{i}", default=defaults_stars[i] if i < len(defaults_stars) else 0.0)

            st.text_area(t("scouting_notes", L), key=comment_key, height=90, placeholder="…")

            improve_key    = f"{key_prefix}_{i}_improve"
            suggestion_key = f"{key_prefix}_{i}_suggestion"
            col_btn, _ = st.columns([1, 4])
            with col_btn:
                if st.button(f"✨ {t('improve', L)}", key=improve_key):
                    raw = st.session_state[comment_key]
                    if raw.strip():
                        with st.spinner(f"{t('improving', L)}"):
                            st.session_state[suggestion_key] = improve_text(raw)
                    else:
                        st.warning(t("nothing_to_improve", L))
            if st.session_state.get(suggestion_key):
                suggestion = st.session_state[suggestion_key]
                st.markdown(f"**{t('suggested_improvement', L)}**")
                st.text_area("Suggested", value=suggestion, height=90, key=f"{key_prefix}_{i}_sug_display", label_visibility="collapsed")
                _, col_accept, col_discard, _ = st.columns([1, 1.5, 1.5, 1])
                with col_accept:
                    if st.button(t("accept", L), key=f"{key_prefix}_{i}_accept", type="primary", use_container_width=True):
                        st.session_state[f"{key_prefix}_{i}_accept_pending"] = True
                        st.rerun()
                with col_discard:
                    if st.button(t("discard", L), key=f"{key_prefix}_{i}_discard", use_container_width=True):
                        st.session_state[suggestion_key] = ""
                        st.rerun()

        star_values.append(val)
        comments.append(st.session_state[comment_key])
        video_data.append(st.session_state[video_key])

    return star_values, comments, video_data


