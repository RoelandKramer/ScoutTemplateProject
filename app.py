# ─── AI text improvement ────────────────────────────────────────────────────

def _get_anthropic_key() -> str | None:
    try:
        key = st.secrets.get("ANTHROPIC_API_KEY", "")
        if key and not key.startswith("#"):
            return key
    except Exception:
        pass
    return os.environ.get("ANTHROPIC_API_KEY") or None


def improve_text(text: str) -> str:
    api_key = _get_anthropic_key()
    if not api_key:
        st.error("No Anthropic API key.")
        return text
    try:
        import anthropic
        client = anthropic.Anthropic(api_key=api_key)
        msg = client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=512,
            messages=[{
                "role": "user",
                "content": (
                  "Spelling/Structuur check! "
                  "Geef uitsluitend de verbeterde tekst terug, zonder introductie of uitleg.\n\n"
                    f"Input: {text}\n"
                    "Output:"
                ),
            }],
        )
        return msg.content[0].text.strip()
    except Exception as exc:
        st.error(f"AI improvement failed: {exc}")
        return text



# ─── Authentication ──────────────────────────────────────────────────────────

def _get_all_users() -> dict[str, dict]:
    """Return all users from secrets as {username: {email, ...}}."""
    try:
        return dict(st.secrets.get("users", {}))
    except Exception:
        return {}


def _authenticate(login_input: str, password: str) -> str | None:
    try:
        users = st.secrets.get("users", {})
        user = users.get(login_input)
        if user and user.get("password") == password:
            return login_input
        for uname, udata in users.items():
            if udata.get("email", "").lower() == login_input.lower() and udata.get("password") == password:
                return uname
    except Exception:
        pass
    return None


